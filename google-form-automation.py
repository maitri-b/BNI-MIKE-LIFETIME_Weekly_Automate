    def clean_amount(self, amount_str):
        """‡∏ó‡∏≥‡∏Ñ‡∏ß‡∏≤‡∏°‡∏™‡∏∞‡∏≠‡∏≤‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏¢‡∏≠‡∏î‡πÄ‡∏á‡∏¥‡∏ô"""
        if not amount_str:
            return "0"
        cleaned = re.sub(r'[^\d,.]', '', str(amount_str))
        cleaned = cleaned.replace(',', '')
        return cleaned if cleaned else "0"

